/// ValueType represents the allowed value types for a Tailwind CSS prefix
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
#[allow(dead_code)] // Not using Length yet
pub enum ValueType {
    None,           // For standalone utilities with no values
    Number,         // For numeric values: m-4, p-2
    Fraction,       // For fractional values: w-1/2, h-3/4
    Color,          // For direct color values: bg-white
    ColorPalette,   // For color palettes: bg-red-500
    CustomProperty, // For CSS custom properties: bg-(--var)
    ArbitraryValue, // For arbitrary values: p-[10px]
    Length,         // For length values
    Scale,          // For size scales: text-sm, rounded-lg
}

/// TailwindPrefix stores metadata about a Tailwind CSS class prefix
#[derive(Debug)]
pub struct TailwindPrefix {
    pub order: usize,
    pub prefix: &'static str,
    #[allow(dead_code)]
    pub value_types: &'static [ValueType],
    #[allow(dead_code)]
    pub allowed_values: &'static [&'static str],
    pub is_standalone: bool,
}

